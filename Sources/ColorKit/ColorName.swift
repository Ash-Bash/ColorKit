import Foundation

struct ColorName {
    
    // Variables
    public var name: String
    public var red: Double
    public var green: Double
    public var blue: Double
    public var alpha: Double
    
    public init(name: String, red: Double, green: Double, blue: Double, alpha: Double) {
        self.name = name
        self.red = red
        self.green = green
        self.blue = blue
        self.alpha = alpha
    }
    
}
